    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        // metodo para adicionar no estoque
        try {
            int qtd_atual, qtd;
            qtd_atual = Integer.valueOf(txtEstoque.getText());
            qtd =  Integer.valueOf(txtQtd.getText());
            qtd_atual = qtd_atual + qtd;
            ProdutoDAO dao = new ProdutoDAO();
            dao.adicionarEstoque(idProduto, qtd_atual);
            JOptionPane.showMessageDialog(null, "Produto Adicionado no Estoque");
            new Utilitarios().limpaTela(painelConsultaClientes);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro: "+e);
        }
        
    }//GEN-LAST:event_btAddActionPerformed

